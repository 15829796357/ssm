package com.how2java.pojo;

public class LiveBroadcastPojo {

	private int  lb_id;
	private String  title;
	private int  lb_time;
	private int  lb_signtime;
	private String  lb_content;
	private String  lb_url;
	private String  lb_handout;
	public int getLb_id() {
		return lb_id;
	}
	public void setLb_id(int lb_id) {
		this.lb_id = lb_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getLb_time() {
		return lb_time;
	}
	public void setLb_time(int lb_time) {
		this.lb_time = lb_time;
	}
	public int getLb_signtime() {
		return lb_signtime;
	}
	public void setLb_signtime(int lb_signtime) {
		this.lb_signtime = lb_signtime;
	}
	public String getLb_content() {
		return lb_content;
	}
	public void setLb_content(String lb_content) {
		this.lb_content = lb_content;
	}
	public String getLb_url() {
		return lb_url;
	}
	public void setLb_url(String lb_url) {
		this.lb_url = lb_url;
	}
	public String getLb_handout() {
		return lb_handout;
	}
	public void setLb_handout(String lb_handout) {
		this.lb_handout = lb_handout;
	}
	

}
